def random_personality():
    return {
        "extraversion": round(random.uniform(0.1, 0.99), 2),
        "openness": round(random.uniform(0.1, 0.99), 2),
        "agreeableness": round(random.uniform(0.1, 0.99), 2),
        "conscientiousness": round(random.uniform(0.1, 0.99), 2),
        "neuroticism": round(random.uniform(0.1, 0.99), 2),
        "assertiveness": round(random.uniform(0.1, 0.99), 2),
        "creativity": round(random.uniform(0.1, 0.99), 2),
        "curiosity": round(random.uniform(0.1, 0.99), 2),
        "risk_taking": round(random.uniform(0.1, 0.99), 2),
        "empathy": round(random.uniform(0.1, 0.99), 2),
    }

def random_travel_preferences():
    return {
        "travel_style": random.choice(["adventure", "luxury", "cultural", "nature-focused", "budget"]),
        "preferred_companion": random.choice(["solo", "group", "quiet"]),
        "budget_level": random.choice(["budget", "mid-range", "luxury"]),
        "preferred_destinations": random.sample(["tropical", "city", "mountain", "desert", "beach", "historic"], 2),
        "travel_frequency": random.choice(["frequent", "occasional", "rare"]),
        "eco_friendly_travel": random.choice([True, False]),
        "adaptability": round(random.uniform(0.1, 0.95), 2)
    }

def generate_bio(name, interests, location):
    templates = [
        f"{name} from {location} loves {random.choice(interests)} and is always looking for new adventures.",
        f"Based in {location}, {name} enjoys {', '.join(random.sample(interests, 2))} and exploring new cultures.",
        f"{name} has a passion for {random.choice(interests)} and making memories while traveling.",
        f"Living in {location}, {name} spends free time {random.choice(['exploring', 'learning', 'creating'])} and {random.choice(interests)}.",
    ]
    return random.choice(templates)

# ----------------------------
# Main Generation
# ----------------------------
users = []
for i in range(5000):
    # Location details
    loc_data = random.choice(location_data)
    city = loc_data["city"]
    country = loc_data["country"]
    languages = loc_data["languages"]

    # Age-based education & occupation
    age = random.randint(20, 60)
    if age < 23:
        education = random.choice(["High School", "Bachelor"])
        occupation = random.choice(["Student", "Intern", "Junior " + random.choice(occupations)])
    elif age < 30:
        education = random.choice(["Bachelor", "Master"])
        occupation = random.choice(occupations)
    else:
        education = random.choice(education_levels)
        occupation = random.choice(occupations)

    user_profile = {
        "id": str(uuid.uuid4()),
        "name": f"User{i}",
        "age": age,
        "gender": random.choice(genders),
        "location": city,
        "country": country,
        "education": education,
        "occupation": occupation,
        "marital_status": random.choice(marital_statuses),
        "learning_style": random.choice(learning_styles),
        "humor_style": random.choice(humor_styles),
        "interests": random.sample(interests_list, k=random.randint(5, 10)),
        "personality": random_personality(),
        "languages": list(set(random.sample(languages + ["en"], k=random.randint(1, len(languages))))),
        "bio": generate_bio(f"User{i}", interests_list, city),
        "travel_preferences": random_travel_preferences()
    }

    # Make ~20% of profiles less rich
    if random.random() < 0.2:
        keys_to_remove = random.sample(list(user_profile.keys()), k=random.randint(4, 6))
        for k in keys_to_remove:
            if k not in ["id", "name", "age", "location", "country"]:
                user_profile.pop(k, None)

    users.append(user_profile)

# Save to file
with open("data/travel_ready_user_profiles.json", "w", encoding="utf-8") as f:
    json.dump(users, f, indent=4, ensure_ascii=False)

